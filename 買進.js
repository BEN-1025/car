// 一鍵下單功能
function purchaseCarbonCredits() {
    const price = parseFloat(priceInput.value);
    const quantity = parseFloat(quantityInput.value);

    if (isNaN(price) || isNaN(quantity) || price <= 0 || quantity <= 0) {
        alert("請輸入有效的價格和數量");
        return;
    }

    // 呼叫智能合約的 listCarbonCredit 方法
    const web3 = new Web3(Web3.givenProvider);
    const contract = new web3.eth.Contract(contractABI, contractAddress);

    web3.eth.getAccounts().then(accounts => {
        const account = accounts[0];

        // 檢查代幣餘額是否足夠
        contract.methods.checkBalance(account).call().then(balance => {
            const totalCost = price * quantity;
            const fee = totalCost * 0.05;
            const totalAmount = totalCost + fee;

            if (balance >= totalAmount) {
                // 列出碳權
                contract.methods.listCarbonCredit(0, quantity, price, true).send({ from: account })
                    .on('receipt', function(receipt) {
                        console.log('listCarbonCredit 成功', receipt);
                        addOrderToTable(price, quantity, fee);
                        alert('下單成功！');
                        updateOrderList();
                    })
                    .on('error', function(error) {
                        console.error('listCarbonCredit 失敗', error);
                        alert('下單失敗，請稍後再試。');
                    });
            } else {
                alert('代幣餘額不足。');
            }
        });
    });
}

// 將下單資料顯示在表格中
function addOrderToTable(price, quantity, fee) {
    const row = document.createElement('tr');
    const priceCell = document.createElement('td');
    const quantityCell = document.createElement('td');
    const feeCell = document.createElement('td');
    priceCell.textContent = price;
    quantityCell.textContent = quantity;
    feeCell.textContent = fee.toFixed(2);
    row.appendChild(priceCell);
    row.appendChild(quantityCell);
    row.appendChild(feeCell);
    orderTableBody.appendChild(row);

    updateOrderList();   
}

