// 從智能合約中獲取訂單資訊
function loadOrders() {
    const contract = new web3.eth.Contract(contractABI, contractAddress);

    contract.methods.nextListingId().call().then(nextListingId => {
        for (let i = 0; i < nextListingId; i++) {
            contract.methods.listings(i).call().then(listing => {
                addOrderToTable(listing.tokenId, listing.amount, listing.price, listing.seller, listing.isBuyOrder);
            });
        }
    });
}

